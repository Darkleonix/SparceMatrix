public class Foothill {

	final static int MAT_SIZE = 100000;

	public static void main(String[] args) throws Exception {
		
		// 100000 x 100000 filled with 0
		int k;
		SparseMat mat = new SparseMat(MAT_SIZE, MAT_SIZE, 0.);

		// test mutators
		for (k = 0; k < 10; k++) {
			mat.set(k, k, k * 1.);
			mat.set(4, k, k * 10.);
			mat.set(k, 4, -k * 10.);
		}
		
		System.out.println("12x12 subsquare of Sparse Matrix \n");
		mat.showSubSquare(0, 12);
		System.out.println();

		SparseMat mat2 = (SparseMat) mat.clone();

		for (k = 0; k < 10; k++) {
			mat.set(k, k, 1.);
			mat.set(4, k, 10.);
			mat.set(k, 4, -10.);
		}

		System.out.println("After chaning 12x12 subsquare of Sparse Matrix \n ");
		mat.showSubSquare(0, 12);
		System.out.println();
		System.out.println(" After cloning 12x12 Matrix: \n");
		mat2.showSubSquare(0, 12);
	}
}

